import pkg from 'pg';
const { Client } = pkg;
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Read .env file manually
const envPath = path.resolve(__dirname, '../.env');
const envFile = fs.readFileSync(envPath, 'utf8');

const envConfig = {};
envFile.split('\n').forEach(line => {
  const match = line.match(/^([^=]+)=(.*)$/);
  if (match) {
    envConfig[match[1]] = match[2].trim();
  }
});

const DB_PASSWORD = envConfig['SUPABASE_DB_PASSWORD'];
const SUPABASE_PROJECT_ID = envConfig['VITE_SUPABASE_URL']?.split('//')[1]?.split('.')[0];

const connectionString = `postgresql://postgres:${encodeURIComponent(DB_PASSWORD)}@db.${SUPABASE_PROJECT_ID}.supabase.co:5432/postgres`;
const client = new Client({ connectionString });

async function updateSchema() {
  try {
    await client.connect();
    console.log('Connected to Supabase PostgreSQL database.');

    const sql = `
      -- 1. Lead History Table
      CREATE TABLE IF NOT EXISTS public.lead_history (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        lead_id BIGINT REFERENCES public.leads(id) ON DELETE CASCADE,
        note TEXT NOT NULL,
        type TEXT DEFAULT 'not', -- 'not', 'durum_degisikligi', 'sistem'
        created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
      );

      -- 2. RLS for lead_history
      ALTER TABLE public.lead_history ENABLE ROW LEVEL SECURITY;
      DROP POLICY IF EXISTS "Enable all operations for all users" ON public.lead_history;
      CREATE POLICY "Enable all operations for all users" ON public.lead_history FOR ALL USING (true);

      -- 3. Initial history from existing leads
      INSERT INTO public.lead_history (lead_id, note, type, created_at)
      SELECT id, reaction, 'not', created_at FROM public.leads
      ON CONFLICT DO NOTHING;
    `;

    await client.query(sql);
    console.log('Schema updated successfully!');
  } catch (err) {
    console.error('Error updating schema:', err);
  } finally {
    await client.end();
  }
}

updateSchema();
