import pkg from 'pg';
const { Client } = pkg;
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Read .env file manually
const envPath = path.resolve(__dirname, '../.env');
const envFile = fs.readFileSync(envPath, 'utf8');

const envConfig = {};
envFile.split('\n').forEach(line => {
  const match = line.match(/^([^=]+)=(.*)$/);
  if (match) {
    envConfig[match[1]] = match[2].trim();
  }
});

const DB_PASSWORD = envConfig['SUPABASE_DB_PASSWORD'];
const SUPABASE_PROJECT_ID = envConfig['VITE_SUPABASE_URL']?.split('//')[1]?.split('.')[0];

const connectionString = `postgresql://postgres:${encodeURIComponent(DB_PASSWORD)}@db.${SUPABASE_PROJECT_ID}.supabase.co:5432/postgres`;
const client = new Client({ connectionString });

async function createLogTable() {
  try {
    await client.connect();
    console.log('Connected to Supabase PostgreSQL database.');

    const sql = `
      -- Activity Log Table
      CREATE TABLE IF NOT EXISTS public.activity_log (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_name TEXT NOT NULL,
        action TEXT NOT NULL,
        details TEXT,
        target_name TEXT,
        created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
      );

      -- RLS for activity_log
      ALTER TABLE public.activity_log ENABLE ROW LEVEL SECURITY;
      DROP POLICY IF EXISTS "Enable all operations for all users" ON public.activity_log;
      CREATE POLICY "Enable all operations for all users" ON public.activity_log FOR ALL USING (true);
    `;

    await client.query(sql);
    console.log('Activity log table created!');
  } catch (err) {
    console.error('Error creating log table:', err);
  } finally {
    await client.end();
  }
}

createLogTable();
